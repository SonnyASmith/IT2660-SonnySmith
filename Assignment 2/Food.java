
/**
 * A class that will represent food as objects with a name value. 
 *
 * @author Sonny Smith
 * @version 09/28/21
 */
public class Food
{
    private String name;

    public Food(String name)  //Creates a food object
    {
      this.name = name;
    }
    
    public String getName() // Retrieves the name of the food object
    {
      return this.name;
    }
    
}
